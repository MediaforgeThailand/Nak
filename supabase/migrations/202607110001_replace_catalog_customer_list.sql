upserted_products as (
  insert into public.products (category_id, sku, name, unit, price, is_active, sort_order)
  select c.id, p.sku, p.name, p.unit, p.price::numeric, true, p.sort_order
  from seed_products p
  join upserted_categories c on c.name = p.category_name
  on conflict (sku) do update
    set category_id = excluded.category_id,
        name = excluded.name,
        unit = excluded.unit,
        price = excluded.price,
        is_active = true,
        sort_order = excluded.sort_order
  returning id, sku
),
ensured_inventory as (
  insert into public.inventory (product_id, quantity_available, low_stock_threshold)
  select id, 0, 5
  from upserted_products
  on conflict (product_id) do nothing
  returning product_id
),
deactivated_old_products as (
  update public.products
  set is_active = false,
      updated_at = now()
  where is_active = true
    and sku not in (select sku from seed_products)
  returning id
)
